# Variable tabby_cat declared and assigned a string value.  The output will be displayed
# in tabbed format symbolized with the "\t" at the start of the string.
tabby_cat = "\tI'm tabbed in."

# Variable persian_cat declared and assigned the string value below.
# The output will be displayed on two separate lines as indicated by the "\n" symbol
persian_cat = "I'm split\non a line."

# Variable backlash_cat declared and assigned the string value below.
# the output will be displayed with a backslash where the "\\" symbol is shown.
backslash_cat = "I'm \\ a \\ cat."

# Variable fat_cat assigned a string input of multiple lines indicated by the """'s
# the output display will be tabbed for each item indicated by the "/t"
# each item will be output in a new line (due to the last item being in the same line
# as the previous item the "\n" symbol was used to display that item in a new line.)
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip \n\t* Grass
"""
# Print out the variable tabby_cat
puts tabby_cat

# Print out the variable persian_cat
puts persian_cat

# Print out the variable backslash_cat
puts backlash_cat

# Print out the variable fat_cat
puts fat_cat

